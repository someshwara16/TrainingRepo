package com.model;

public class StringModel {

	private String str1;
	private int[] array;

	public StringModel() {
		super();
	}

	public StringModel(String str1, int[] array) {
		super();
		this.str1 = str1;
		this.array = array;
	}

	public String getStr1() {
		return str1;
	}

	public void setStr1(String str1) {
		this.str1 = str1;
	}

	public int[] getArray() {
		return array;
	}

	public void setArray(int[] array) {
		this.array = array;
	}

}
